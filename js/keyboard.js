window.addEventListener('keyup', (e) => {
    if (e.key == '0') {
        link('#zero-button').click();
    }
    if (e.key == '1') {
        link('#one-button').click();
    }
    if (e.key == '2') {
        link('#two-button').click();
    }

    if (e.key == '3') {
        link('#three-button').click();
    }

    if (e.key == '4') {
        link('#four-button').click();
    }

    if (e.key == '5') {
        link('#five-button').click();
    }

    if (e.key == '6') {
        link('#six-button').click();
    }

    if (e.key == '7') {
        link('#seven-button').click();
    }

    if (e.key == '8') {
        link('#eight-button').click();
    }

    if (e.key == '9') {
        link('#nine-button').click();
    }
    if (e.key == '+') {
        link('#-button').click();
    }

    if (e.key == '-') {
        link('#-button').click();
    }

    if (e.key == '*') {
        link('#-button').click();
    }

    if (e.key == '/') {
        link('#-button').click();
    }

    if (e.key == '%') {
        link('#-button').click();
    }

    if (e.key == 'Enter') {
        link('#-button').click();
    }

    if (e.key == 'Escape') {
        link('#-button').click();
    }

    if (e.key == 's' || e.key == 'S') {
        link('#-button').click();
    }

    if (e.key == '.') {
        link('#-button').click();
    }
});